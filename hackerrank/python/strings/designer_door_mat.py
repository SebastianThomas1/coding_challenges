# Designer Door Mat

def designer_door_mat(n):
    return '\n'.join(
        ['-' * (3*(n//2 - idx)) + '.|.' * (2*idx + 1)
         + '-' * (3*(n//2 - idx)) for idx in range(n // 2)]
        + ['-' * (3*(n//2) - 2) + 'WELCOME' + '-' * (3*(n//2) - 2)]
        + ['-' * (3 * (n // 2 - idx)) + '.|.' * (2 * idx + 1)
           + '-' * (3 * (n // 2 - idx)) for idx in range(n // 2 - 1, -1, -1)])



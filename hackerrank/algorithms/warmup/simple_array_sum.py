# Simple Array Sum

def simple_array_sum(ar):
    return sum(ar)


if __name__ == '__main__':
    print(simple_array_sum([1, 2, 3]))  # 6
    print(simple_array_sum([1, 2, 3, 4, 10, 11]))  # 31
